// Q15.Write a java program to check whether vehicle speed exceeds 100 km/h.


import java.util.*;
public class p15{
public static void main(String[] args)
{
    Scanner rs=new Scanner(System.in);
    int js=rs.nextInt();
    if(js>100)
    {
        System.out.println("speed");
    }
    else
    {
       System.out.println("limit speed");
    }
}
}
