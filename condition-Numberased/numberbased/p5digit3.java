package Numberased.numberbased;
 import java.util.*;

public class p5digit3 
{
   public static void main(String[] args) 
   {
    Scanner rs=new Scanner(System.in);
    int js=rs.nextInt();
    if(js>=100 && js<=999)
    {
         
        System.out.println("digit");
    }
    else
    {
        System.out.println("Not digit");
   } 
   }
}
